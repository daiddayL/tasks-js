const arr = [1, 2, 3, "a", "b", "c"];

// Методы, которые изменяют исходный массив:
arr.push(); 
arr.unshift(); 
arr.pop(); 
arr.shift(); 
arr.reverse(); 
arr.splice();
arr.copyWithin(); 

// Методы, которые НЕ изменяют исходный массив:
arr.at(); 
arr.join(); 
arr.indexOf(); 
arr.lastIndexOf(); 
arr.includes(); 
arr.flat(); 
arr.concat(); 
arr.slice(); 